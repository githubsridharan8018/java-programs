package com.mywork;

public class Relational_Operator {

	public static void main(String[] args) {

		
		int a=20;
		int b=30;
		
		
		System.out.println("equal to -- " + (a==b));   //equal and not equal to
		
		
		System.out.println("not equal --" + (a!=b));
		
		
		
		
		System.out.println("greater than --" + (a>b));//greater than and less than
		
		System.out.println("less than -- " + (a<b));
		
		
		
		System.out.println("greater than or equal to --" + (a>=b));//greater than or equal to and less tha or equal to 
		
		System.out.println("less than or equal to -- " + (a<=b));
		
		
			
		

	}

}
