package com.mywork;

public class Arithmetic_Logic_Operator {

	public static void main(String[] args) {//Arithmetic logic operator
     
		
		int a=6;
		int b=4;
		int c=5;
		
		a*=6;// can be written as  a=a*6;
	
		
		b*=4; // can be written as b=b*4;
		
		c*=5;// can be written as c=c*5;
		
		
		System.out.println("the value of a is"+" " +a);
		

		System.out.println("the value of b is"+" " +b);
		

		System.out.println("the value of c is"+" " +c);
		
		
		
		
	}

}
