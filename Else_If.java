package com.mywork;

public class Else_If {

	public static void main(String[] args) {// its a block and we can given multiple conditions

		int a=10;
		
		
		
		
		if (a>=20) {
			System.out.println("One");
		}
		
		
		
		else if (a<=5) {
			
			
			System.out.println("Two");
			
		}
	
		else if (a<=6) {
			
			System.out.println("Three");
		}
		
		else {
			System.out.println("terminate and exit loop");
		}
	}
	
	

}

